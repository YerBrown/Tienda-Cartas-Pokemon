class ShopProduct {
  constructor(id, name, set, productType, imageUrl, price, packsAmount) {
    this.id = id;
    this.name = name;
    this.set = set;
    this.productType = productType;
    this.imageUrl = imageUrl;
    this.price = price;
    this.packsAmount = packsAmount
  }
}
export default ShopProduct;
